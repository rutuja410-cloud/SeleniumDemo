package array;

public class FloatArray
{
    public static void main(String[] args)
    {
        float[] prices = {10.5f, 20.75f, 15.25f, 34.90f};

        System.out.println("float values : ");

        for(float p : prices)
        {
            System.out.println(p);
        }

        String[] colors = {"Red", "Pink", "blue"};
        for(String c : colors)
        {
            System.out.println(c);
        }



    }

}
