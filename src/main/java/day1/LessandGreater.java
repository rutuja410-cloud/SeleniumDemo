package day1;

public class LessandGreater
{
    public static void main(String[] args)
    {
        int a=10, b=5, c=10;

        //comparing a & b
        System.out.println("a>b ==> "+(a>b));   //true

        System.out.println("a>b ==> "+(a>=b));  // true

        //coparing a & c

        System.out.println("a>c ==> "+(a<c));  // false

        System.out.println("a>c ==> "+(a<=c)); // true

    }
}
