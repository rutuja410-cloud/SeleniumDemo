package loops;

public class NaturalNumber
{
    public static void main(String args[]) {
        System.out.println("Natural Number");

        for (int i = 1; i <= 10; i++) {
            System.out.print(i +" ");
        }

        System.out.println("Reverse number");

        for (int i = 10; i >= 1; i--)
        {
            System.out.print(i +" ");
        }

   }
}
